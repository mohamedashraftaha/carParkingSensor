/**
* @brief Function implementing the myTask03 thread.
* @param argument: Not used
* @retval None
*/
/* USER CODE END Header_buzzerTask */
void buzzerTask(void *argument)
{
  /* USER CODE BEGIN buzzerTask */
	float distance;
	uint8_t task_buffer[30];
	HAL_TIM_PWM_Start(&htim2, TIM_CHANNEL_1);
	TIM2->CCR1 = CCR_val; //50% duty cycle
	
	// for loudness
	float dutyCycleFactor;
	/*Struct for predefined dutyCycles*/
	typedef struct dutyCycles dutyCycles;
	struct dutyCycles{
	float _3_5;
	float _6_10;
	float _10_20;
	float others;
	};
	
	/* Reference for the duty cycle loudness part:
	http://justanotherlanguage.org/content/jallib/tutorials/tutorial_pwm2*/
	dutyCycles DC;
	DC._3_5 = 49.0;
	DC._6_10 = 30.0;
	DC._10_20 = 15.0;
	DC.others = 5.0;
	
  /* Infinite loop */
  

	for(;;)
  {
		/* Acquiring the binarysemaphore released (Given) in the ISR*/
		if (osSemaphoreAcquire(event_signalHandle, HAL_MAX_DELAY) == osOK){
				// range of the HC-SR04 2cm to 400cm

			/*After acquiring the semaphore we will know get the last element in the distance queue handle*/
			if(osMessageQueueGet(distanceQueueHandle, &distance, NULL, HAL_MAX_DELAY) == osOK && (distance >=2 && distance <= 400)){
				
				/*Sending thedistance extracted from the queue over UART
				*To make sure that there is syncronization between the distance 
				* received from the ISR via HC-SR04 and the this task (buzzerTask)
				*/
				taskENTER_CRITICAL();
				sprintf((char*) task_buffer, "Distance TASK: %0.1f\r\n", distance);						
				HAL_UART_Transmit(&huart2, task_buffer, sizeof("Distance TASK: %0.1f\r\n"), 30);	
				taskEXIT_CRITICAL();
				
				/* flag for the minimum distance*/
				minDistFlag = (distance == min_dist1 || distance == min_dist2 );

				// minimum distance
				if(minDistFlag){
					/*If we have minimum distance in our case 2
					* we have default values of ARR -> Which is 50
					* and the compare capture register responsible for the duty cycle
					* as 50%
					*/
					ARR_val = 99;
					CCR_val = 49;
					beep_flag = 1;
				}
				else{
			
				/* We have distances different from the minimum distance */ 
				/* I created a struct for each group of distances where I specify the CCR
				* Decreasing the CCR value as we go further from the HC-SR04
				* i.e, Distance Increases
				* The purpose of this struct is simply to control the loudness "Optional"
				*/
					
				dutyCycleFactor = (distance >=2 && distance<=5) ? DC._3_5 : (distance >=6 && distance<=10)?
				DC._6_10 : (distance >= 10 && distance <=20) ? DC._10_20 : DC.others;
				
				/*For each distance I set the period of each clock cycle 
				* since we know that the ARR controls the period 
				* so the larger the distance the longer the period
				* then I add 100 (My choice) so that I can make a more resonable ARR val
				* i.e of the distance is 4 then the ARR is at 104
				*/	
				ARR_val = (distance) +100 ;
					
				/*This shorthand-if conrtols the beeping, if the beepflag is 1, meaning that
				*it is not the pre-defined minimum distance then I set the CCR = 0. i.e I shut it down
				* else I set the CCR value responsible for the duty cycle based on, the group that the 
				* distance belongs to
				*/
				CCR_val = (beep_flag) ? 0 : (ARR_val * (dutyCycleFactor/100));
				/* if it is beeping, then invert it to mimic a square wave. i.e, 1 0 1 0 1 0 ....*/
				beep_flag = !beep_flag;
					}

				/*Set the timer ARR "Period" to the current ARR decided on based on the if condition*/
				TIM2->ARR = ARR_val;
				/*Set the timer CCR "Duty Cycle" to the current CCR decided on based on the if condition*/
				TIM2->CCR1 = CCR_val;
				}
				/*Zeroing out the buffer*/ 
				memset(task_buffer, 0,sizeof("Distance TASK: %0.1f\r\n"));
				/*Waiting for short time*/
				HAL_Delay(1);
				
				/* Since beep delay is shared between the buzzer task and the ultrasonic sensor task
				* I guard it using task_ENTER/EXIT_Critical 
				* To eliminate the possibility of race conditions
				*/
				taskENTER_CRITICAL();
				beepDelay = distance + 100;
				osDelay(beepDelay + 100);
				
				taskEXIT_CRITICAL();
			}
		}

  }
  /* USER CODE END buzzerTask */


