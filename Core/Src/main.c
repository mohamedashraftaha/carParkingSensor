/**
* @brief Function implementing the myTask02 thread.
* @param argument: Not used
* @retval None
*/
/* USER CODE END Header_ultrasensorTask */
void ultrasensorTask(void *argument)
{
  /* USER CODE BEGIN ultrasensorTask */
  /* Infinite loop */
  for(;;)
  {
		// pull TRIG high
		HAL_GPIO_WritePin(Trig_GPIO_Port, Trig_Pin, GPIO_PIN_SET);
		__HAL_TIM_SetCounter(&htim1, 0);
		//create 10 microsec delay by setting the TIM Counter Register value to 0
		while ( __HAL_TIM_GetCounter(&htim1) < 10/*TIM Counter Register value is less than the intended value */);
		// pull TRIG low
		HAL_GPIO_WritePin(Trig_GPIO_Port, Trig_Pin, GPIO_PIN_RESET);
		// start TIM Input Capture measurement in interrupt mode
		HAL_TIM_IC_Start_IT(&htim1, TIM_CHANNEL_1);
		// wait for a little time
	//	HAL_Delay(20);
			HAL_Delay(1);

		taskENTER_CRITICAL();
    osDelay(beepDelay+100);
		taskEXIT_CRITICAL();
  }
  /* USER CODE END ultrasensorTask */
}

